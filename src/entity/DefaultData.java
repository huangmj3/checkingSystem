package entity;
//默认数据实体类
public class DefaultData {
    //id作为主键
    int id;
    //计划销售额
    int planedSaleroom;
    //基本工资
    int basicSalary;
    //计划完成工资
    int planedSalary;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPlanedSaleroom() {
        return planedSaleroom;
    }

    public void setPlanedSaleroom(int planedSaleroom) {
        this.planedSaleroom = planedSaleroom;
    }

    public int getBasicSalary() {
        return basicSalary;
    }

    public void setBasicSalary(int basicSalary) {
        this.basicSalary = basicSalary;
    }

    public int getPlanedSalary() {
        return planedSalary;
    }

    public void setPlanedSalary(int planedSalary) {
        this.planedSalary = planedSalary;
    }
}
